int itoa_vitaut(INT64 val, char* dst, size_t dsize)
{
    INT64 v1, v2, m;
    v1 = (val < 0) ? -val : val;

    char buf[ITOA_BUFSIZE]; // Max length of UINT64 is 20 symbols, + sign, + null string terminator
    buf[sizeof(buf)-1] = '\0';
    int i = sizeof(buf)-3;
    while (v1 >= 100)
    {
        v2 = v1 / 100;
        m = v1 - (100 * v2);
        memcpy(&buf[i], &str100p[m], sizeof(UINT16));
        i -= 2;
        v1 = v2;
        if (i < 0) return 0;
    }
    memcpy(&buf[i], &str100p[v1], sizeof(UINT16));
    if (v1 < 10) i++;
    if (val < 0) buf[--i] = '-';

    size_t len = sizeof(buf) - i;

    if (len <= dsize)
    {
        memcpy(dst, &buf[i], len);
        return (int)(len - 1);
    }
    return 0;
}
